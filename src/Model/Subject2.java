public class Subject2  {
    public String mamon;
    public String tenmon;
    public String mota;
    public Float diemQuaMon;

    public Subject2(String mamon, String tenmon, String mota, Float diemQuaMon) {
        this.mamon = mamon;
        this.tenmon = tenmon;
        this.mota = mota;
        this.diemQuaMon = diemQuaMon;
    }

    public Subject2() {
    }

    public String getMamon() {
        return mamon;
    }

    public void setMamon(String mamon) {
        this.mamon = mamon;
    }

    public String getTenmon() {
        return tenmon;
    }

    public void setTenmon(String tenmon) {
        this.tenmon = tenmon;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }

    public Float getDiemQuaMon() {
        return diemQuaMon;
    }

    public void setDiemQuaMon(Float diemQuaMon) {
        this.diemQuaMon = diemQuaMon;
    }

   
    
}
